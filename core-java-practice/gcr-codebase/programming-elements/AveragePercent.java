
public class AveragePercent {
	public static void main(String[] args) {
		//input variables
		int mathMarks=94;
		int physicsMarks=95;
		int chemistryMarks=96;
		//calculation
		int averagePercent=(mathMarks+physicsMarks+chemistryMarks)/3;
		//output
		System.out.println("Sam's average marks in PCM is "+ averagePercent);
	}
}

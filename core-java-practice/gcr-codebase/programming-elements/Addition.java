import java.util.*;
public class Addition {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        // Enter first number
        int a=sc.nextInt();
        // Enter second number
        int b=sc.nextInt();
        System.out.println("Sum of a and b is ");
        System.out.println(a+b);
        
    }
}
